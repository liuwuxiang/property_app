function billChoose(type){
	if(type == 0){
		self.window.location.href = "/property_system/wx/v1.0.0/joinChargeWater";
	}
	else if(type == 1){
        self.window.location.href = "/property_system/wx/v1.0.0/joinElectricityFees";
	}
	else if(type == 2){
        self.window.location.href = "/property_system/wx/v1.0.0/joinGasCharge";
	}
	else if(type == 3){
        self.window.location.href = "/property_system/wx/v1.0.0/joinTVFee";
	}
	else if(type == 4){
        self.window.location.href = "/property_system/wx/v1.0.0/joinBroadbandPayment";
	}
	else if(type == 5){
        self.window.location.href = "/property_system/wx/v1.0.0/joinPropertyFee";
	}
}
